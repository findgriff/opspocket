  static CommandTemplate _t({
    required String id,
    required String name,
    required CommandCategory category,
    required String commandText,
    required String description,
    required List<String> stack,
    required bool dangerous,
    required DateTime now,
  }) {
    return CommandTemplate(
      id: id,
      name: name,
      category: category,
      commandText: commandText,
      placeholders: PlaceholderUtils.extract(commandText),
      dangerous: dangerous,
      isBuiltin: true,
      description: description,
      applicableStack: stack,
      createdAt: now,
      updatedAt: now,
    );
  }
